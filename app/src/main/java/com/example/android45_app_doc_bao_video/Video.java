package com.example.android45_app_doc_bao_video;

public class Video {
    private int video;
    private String ten;

    public Video(int video, String ten) {
        this.video = video;
        this.ten = ten;
    }

    public int getVideo() {
        return video;
    }

    public void setVideo(int video) {
        this.video = video;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }
}
